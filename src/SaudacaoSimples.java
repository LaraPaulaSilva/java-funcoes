public class SaudacaoSimples {

    public static double calcularAreaRetangulo(double largura, double altura) {
        return largura * altura;
    }

    public static void main(String[] args) {
        System.out.println("Área do retângulo (5x10): " + calcularAreaRetangulo(5.0, 10.0)); // Saída: Área do retângulo (5x10): 50.0
    }
}

